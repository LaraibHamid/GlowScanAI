class AppSpacing {
  /// Extra small spacing — used for tight gaps (icons, compact UI)
  static const double xs = 6;

  /// Small spacing — used for small gaps between elements
  static const double sm = 10;

  /// Medium spacing — standard padding for text fields, buttons, cards
  static const double md = 14;

  /// Large spacing — used for section separation and comfortable layout padding
  static const double lg = 20;

  /// Extra large spacing — for big layout breaks (page sections, banners)
  static const double xl = 30;
}

